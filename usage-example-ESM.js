// Usage example for ES Module (import syntax)

import { areaOfCircle, circumferenceOfCircle, volumeOfSphere, volumeOfCone } from './geometry.js';

console.log(areaOfCircle(2));         // 12.8
console.log(circumferenceOfCircle(2)); // 12.8
console.log(volumeOfSphere(2));        // ≈28.84
console.log(volumeOfCone(2, 5));       // ≈11.31
